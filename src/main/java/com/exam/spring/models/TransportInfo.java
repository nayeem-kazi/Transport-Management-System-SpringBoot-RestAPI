package com.exam.spring.models;

public class TransportInfo {
	int vId;
	String type;
	String model;
	String capasity;
	String vNo;
	String vBrand;
	String sts;
	String details;
	double baseFair;
	double perKmFair;
	double otherCost;
	public int getvId() {
		return vId;
	}
	public void setvId(int vId) {
		this.vId = vId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getCapasity() {
		return capasity;
	}
	public void setCapasity(String capasity) {
		this.capasity = capasity;
	}
	public String getvNo() {
		return vNo;
	}
	public void setvNo(String vNo) {
		this.vNo = vNo;
	}
	public String getvBrand() {
		return vBrand;
	}
	public void setvBrand(String vBrand) {
		this.vBrand = vBrand;
	}
	public String getSts() {
		return sts;
	}
	public void setSts(String sts) {
		this.sts = sts;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public double getBaseFair() {
		return baseFair;
	}
	public void setBaseFair(double baseFair) {
		this.baseFair = baseFair;
	}
	public double getPerKmFair() {
		return perKmFair;
	}
	public void setPerKmFair(double perKmFair) {
		this.perKmFair = perKmFair;
	}
	public double getOtherCost() {
		return otherCost;
	}
	public void setOtherCost(double otherCost) {
		this.otherCost = otherCost;
	}

}
